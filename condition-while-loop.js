
/* ----------while loop  ---------------*/
let i = 0

while(i<10)
{
    i++
    console.log(i)
}



/* ---------- do-while loop  ---------------*/
//Example: do...while loop can be used when 
//requirement is to check for a status, check status of empty field validation when trying to log in
console.log("*********** do while loop **********")
let j=0
do{
    j++
} while(j>10);
console.log(j)
